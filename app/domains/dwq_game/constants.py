def get_event_for_turn(turn_index: int) -> dict[str, Any]:
    """根據回合索引 (0-based) 取得對應的歷史事件"""
    if turn_index < 0 or turn_index >= len(EVENTS):
        raise ValueError(f"turn_index {turn_index} out of range [0, {len(EVENTS)})")
    return EVENTS[turn_index]


def get_max_factories_for_city(city_id: str) -> int:
    """城市可建工廠數上限 (取決於 base_prices 陣列長度)"""
    return len(CITIES[city_id]["base_prices"])
