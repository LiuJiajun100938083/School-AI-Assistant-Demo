    // ── 颜色映射 ──
    function adjustColor(hex, amount) {
        hex = hex.replace(/^#/, '');
        if (hex.length === 3) hex = hex.split('').map(function(c) { return c + c; }).join('');
        var r = Math.max(0, Math.min(255, parseInt(hex.substring(0, 2), 16) + amount));
        var g = Math.max(0, Math.min(255, parseInt(hex.substring(2, 4), 16) + amount));
        var b = Math.max(0, Math.min(255, parseInt(hex.substring(4, 6), 16) + amount));
        return '#' + [r, g, b].map(function(x) { return x.toString(16).padStart(2, '0'); }).join('');
    }

    function getColorMapping(ch, tint) {
        switch (ch) {
            case '#': return '#0f172a';
            case '.': return tint;
            case 'h': return adjustColor(tint, 45);
            case 's': return adjustColor(tint, -40);
            case 'w': return '#ffffff';
            case 'b': return '#0f172a';
            case 'p': return '#f472b6';
            case 'm': return '#ef4444';
            case 'g': return '#334155';
            case 'y': return '#fcd34d';
            case 'c': return '#22d3ee';
            case 'z': return '#94a3b8';
            case 'e': return '#22c55e';
            default: return null;
        }
    }

    function drawToBuffer(bufCtx, spriteArray, tint) {
        bufCtx.clearRect(0, 0, 32, 32);
        for (var y = 0; y < 32; y++) {
            var row = spriteArray[y] || '';
            for (var x = 0; x < 32; x++) {
                var ch = row[x];
                var color = getColorMapping(ch, tint);
                if (color) {
                    bufCtx.fillStyle = color;
                    bufCtx.fillRect(x, y, 1, 1);
                }
            }
        }
    }

